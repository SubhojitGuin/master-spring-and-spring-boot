package com.in28minutes.rest.webservices.restful_web_services.user;

import org.springframework.stereotype.Component;

import java.time.LocalDate;
import java.util.ArrayList;
import java.util.List;
import java.util.function.Predicate;

//Dao - Data Access Object - Used to perform save, retrieve, delete and other operations on the object user
@Component
public class UserDaoService {
    private static List<User> users = new ArrayList<>();
    private static int usersCount = 0;
    static {
        users.add(new User(++usersCount, "Subho", LocalDate.now().minusYears(30)));
        users.add(new User(++usersCount, "Ayush", LocalDate.now().minusYears(23)));
        users.add(new User(++usersCount, "Jit", LocalDate.now().minusYears(25)));
    }

    public List<User> findAll() {
        return users;
    }

    public User save(User user) {
        user.setId(++usersCount);
        users.add(user);
        return user;
    }

    public User getOne(int id) {
        Predicate<? super User> predicate = user -> user.getId().equals(id);
        return users.stream().filter(predicate).findFirst().orElse(null);
    }
}
